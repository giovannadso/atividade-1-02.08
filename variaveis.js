/*Diferença entre "var" e "let":
A principal diferença entre "var" e "let" está no escopo das variáveis. Quando uma variável é declarada com "var", ela tem escopo de função ou escopo global, dependendo de onde foi declarada. Isso significa que a variável pode ser acessada em qualquer lugar dentro da função ou globalmente se estiver fora de uma função.
Por outro lado, quando uma variável é declarada com "let", ela tem escopo de bloco. Isso significa que a variável só pode ser acessada dentro do bloco onde foi declarada, como dentro de um loop, condicional ou qualquer outro bloco delimitado por chaves "{}".*/

